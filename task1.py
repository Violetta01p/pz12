# Список міст
cities = ["Київ", "Одеса", "Львів", "Харків", "Житомир"]


# Перетворюємо список на множину для швидкого пошуку
city_set = set(cities)


# Перевіряємо міста
if "Одеса" in city_set:
   print("Місто Одеса є у списку.")
else:
   print("Місто Одеса відсутнє у списку.")


